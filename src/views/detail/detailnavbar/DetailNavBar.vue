<template>
    <nav-bar>
      <div slot="left" @click="backClick" class="back">
        <img src="~assets/img/common/back.svg"></img>
      </div>
      <div slot="center" class="title">
        <div v-for="(item,index) in titles"
             class="title-item"
             :class="{active:index === currentIndex}"
             @click="titleClick(index)"
        >{{item}}</div>
      </div>
    </nav-bar>
</template>

<script>
  import NavBar from "components/common/navbar/NavBar";
  export default {
    name: "DetailNavBar",
    components: {
      NavBar
    },
    data() {
      return {
        titles: ['商品','参数','评论','推荐'],
        currentIndex: 0
      }
    },
    methods: {
      titleClick(myindex) {
        this.currentIndex = myindex
      },
      backClick() {
        //两种方法都可以
        // this.$router.go(-1)
        this.$router.back()
      }
    }
  }
</script>

